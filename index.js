function soma(val1,val2) {
    return val1 + val2
}

const resultadoSoma = soma(1,2);

console.log('Resulta da Soma:', resultadoSoma)


function sub (n1,n2) {
    return n1 - n2
}

const resultadoSub = sub(2,1);

console.log('Resultado da Subtração:', resultadoSub)


function div(n3,n4) {
    return n3 / n4
}

const resultadoDiv = div(4,2);

console.log('Resultado da Divisão:', resultadoDiv)


function multi(val3,val4) {
    return val3 * val4
}

const resultadoMulti = multi(2,2);

console.log('Resultado da Multiplicação:', resultadoMulti)

function pot(n5,n6) {
    return n5 ** n6
}

const resultadoPot = pot(3,3)

console.log('Resultado da Potência:', resultadoPot)